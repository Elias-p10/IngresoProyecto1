    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
    try {
            
            String SQL = "insert into Estudiantes (nombre,apellido,carnet) values (?,?,?)";
            
            PreparedStatement pst = bd.prepareStatement(SQL);
            //wjile es mientras
            while (!inscritos.isEmpty()) { //mientras inscrito no esta vacia
                alumno = inscritos.dequeue();//dequeue que desencole

            pst.setString(1, alumno.getNombre());
            pst.setString(2, alumno.getApellido());
            pst.setInt(3, alumno.getCarnet());
            
            pst.execute();//para que ejecute la consulta y lo guarda en la base de datos
            
            }
            
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error de Registo" + e.getMessage());
            
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

